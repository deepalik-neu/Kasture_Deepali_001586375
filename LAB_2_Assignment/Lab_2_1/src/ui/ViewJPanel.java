    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
         int selectedRowId=tblCarList.getSelectedRow();
         if(selectedRowId<0){
             JOptionPane.showMessageDialog(this, "Please select a row to edit");
         }
        DefaultTableModel model = (DefaultTableModel) tblCarList.getModel();

        txtCarName.setText(String.valueOf(model.getValueAt(selectedRowId, 0)));
       
        txtManufacterdYear.setText(String.valueOf(model.getValueAt(selectedRowId, 4)));
        txtCity.setText(String.valueOf(model.getValueAt(selectedRowId, 3)));
        txtModelNumber.setText(String.valueOf(model.getValueAt(selectedRowId, 1)));
        txtSerialNumber.setText(String.valueOf(model.getValueAt(selectedRowId, 2)));
        chkAvailability.setSelected(Boolean.valueOf(model.getValueAt(selectedRowId, 6).toString()));
        chkIsExpired.setSelected(Boolean.valueOf(model.getValueAt(selectedRowId, 7).toString()));
        txtNumberOfSeats.setText(String.valueOf(String.valueOf(model.getValueAt(selectedRowId, 5))));

    }//GEN-LAST:event_btnEditActionPerformed

