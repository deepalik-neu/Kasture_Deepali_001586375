        // TODO add your handling code here:

         int selectedRowIndex=tblCarList.getSelectedRow();
         if(selectedRowIndex<0){
             JOptionPane.showMessageDialog(this, "Please select a row to update");
         }
        DefaultTableModel model = (DefaultTableModel) tblCarList.getModel();
        Car car=(Car) model.getValueAt(selectedRowIndex,0);
       car.setName(txtCarName.getText());
       car.setManufacturedYear(txtManufacterdYear.getText());
      
       car.setCity(txtCity.getText());
       car.setNumberOfSeats(Integer.parseInt(txtNumberOfSeats.getText()));
       car.setSerialNumber(txtSerialNumber.getText() );
       car.setModelNumber(txtModelNumber.getText());
       car.setIsAvailable(chkAvailability.isSelected());
       car.setIsExpired(chkIsExpired.isSelected());
       JOptionPane.showMessageDialog(this, "Data is successfully updated.");
     SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");  
    Date date = new Date();  
    carList.setLastUpdatedDate(date);
            txtLastUpdatedCatlogDate.setText(formatter.format(carList.getLastUpdatedDate()));
